-- Basic EXTRACT with date literals
SELECT EXTRACT(YEAR FROM DATE '2024-01-15');
SELECT EXTRACT(MONTH FROM DATE '2024-01-15');  
SELECT EXTRACT(DAY FROM DATE '2024-01-15');

-- EXTRACT with timestamp
SELECT EXTRACT(HOUR FROM TIMESTAMP '2024-01-15 10:30:45');
SELECT EXTRACT(MINUTE FROM TIMESTAMP '2024-01-15 10:30:45');
SELECT EXTRACT(SECOND FROM TIMESTAMP '2024-01-15 10:30:45');

-- Complex expression (the original problem case)
SELECT EXTRACT(YEAR FROM COALESCE(TRY_CAST('2024-01-15' AS DATE)));

-- Additional complex cases
SELECT EXTRACT(MONTH FROM COALESCE(TRY_CAST('2024-01-15' AS DATE), DATE '2000-01-01'));

-- EXTRACT with computed expressions
SELECT EXTRACT(YEAR FROM CURRENT_DATE);
SELECT EXTRACT(DAY FROM DATE '2024-01-15' + INTERVAL '7' DAY);

-- Multiple extract fields
SELECT EXTRACT(QUARTER FROM DATE '2024-04-15');
SELECT EXTRACT(WEEK FROM DATE '2024-01-15');
SELECT EXTRACT(DAY_OF_YEAR FROM DATE '2024-04-15');